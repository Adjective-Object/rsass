#[test]
fn t03_inline_double() {
    check(
        ".result {
  output: #{#{\"\\1\\2\\3\\4\\5\\6\\7\\8\\9\"}};
  output: #{\"[#{\"\\1\\2\\3\\4\\5\\6\\7\\8\\9\"}]\"};
  output: #{\"#{\"\\1\\2\\3\\4\\5\\6\\7\\8\\9\"}\"};
  output: #{'#{\"\\1\\2\\3\\4\\5\\6\\7\\8\\9\"}'};
  output: #{\"['#{\"\\1\\2\\3\\4\\5\\6\\7\\8\\9\"}']\"};
}
",
        ".result {
  output: \u{1}\u{2}\u{3}\u{4}\u{5}\u{6}\u{7}\u{8}\u{9};
  output: [\u{1}\u{2}\u{3}\u{4}\u{5}\u{6}\u{7}\u{8}\u{9}];
  output: \u{1}\u{2}\u{3}\u{4}\u{5}\u{6}\u{7}\u{8}\u{9};
  output: \u{1}\u{2}\u{3}\u{4}\u{5}\u{6}\u{7}\u{8}\u{9};
  output: ['\u{1}\u{2}\u{3}\u{4}\u{5}\u{6}\u{7}\u{8}\u{9}'];
}
",
    )
}
